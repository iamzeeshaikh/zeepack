// Build flat location list and lookup map
function buildLocations(): LocationData[] {
  const result: LocationData[] = [];

  for (const state of statesData) {
    const topCities = state.cities.map((c) => ({ name: c.name, slug: c.slug }));

    result.push({
      slug: state.slug,
      name: state.name,
      type: "state",
      stateName: state.name,
      stateAbbr: state.abbr,
      stateSlug: state.slug,
      heroImage: state.heroImage,
      industries: state.industries,
      products: state.products,
      blurb: state.blurb,
      topCities,
    });

    for (const city of state.cities) {
      result.push({
        slug: city.slug,
        name: city.name,
        type: "city",
        stateName: state.name,
        stateAbbr: state.abbr,
        stateSlug: state.slug,
        heroImage: city.heroImage,
        industries: city.industries,
        products: city.products,
        blurb: city.blurb,
        topCities,
      });
    }
  }

  return result;
}

export const allLocations: LocationData[] = buildLocations();

export const locationsBySlug: Record<string, LocationData> = Object.fromEntries(
  allLocations.map((loc) => [loc.slug, loc]),
);

export function getLocationBySlug(slug: string): LocationData | undefined {
  return locationsBySlug[slug];
}

export const locationSlugs: string[] = allLocations.map((loc) => loc.slug);
